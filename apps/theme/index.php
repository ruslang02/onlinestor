<select name="theme">
    <option disabled selected>Выберите тему</option>
    <option value="windows">Windows 7</option>
    <option value="bootstrap-original">BootStrap Original</option>
</select>
<script>
$("[name=theme]").on({
    change: function() {
        confirm('После применения темы все окна будут закрыты, следовательно, вы потеряете все несохраненные данные. Продолжить?');

        $("*[href*='windows.css']").remove();
        $("*[href*='bootstrap-original.css']").remove();
        if($("option:selected").val() == "windows") {$("body").addClass("blur");} else {$("body").removeClass("blur");}
        $("body").html($("body").html() + "<link rel='stylesheet' href='/desktop/css/" + $("option:selected").val() + ".css'>");
$(".window").remove();
        $('*:not(body)').hide();
        setInterval("$('*').show()",1000)
        $("[tipe=task]").remove();
        init();
    }
})
</script>